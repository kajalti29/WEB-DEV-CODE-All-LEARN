let a = "5";
console.log(+a);       // 5   (converted to number)
console.log(-a);       // -5  (converted to number and negated)
console.log(typeof a); // "string"
console.log(!true);    // false

let x = 10;
console.log(++x);      // 11 (pre-increment)
console.log(x--);      // 11 (post-decrement)
console.log(x);        // 10
