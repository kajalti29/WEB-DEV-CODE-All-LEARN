// Sum function (normal)
function sum(a, b) {
    return a + b;
}

// Arrow function for sum (prints the result)
const arrowSum = (a, b) => {
    console.log(a + b);
};

// Multiplication function (normal)
function mul(a ,b){
    return a * b;
}

// Arrow function for multiplication (returns result)
const arrowMul = (a, b)  => {
    return a * b;
}
