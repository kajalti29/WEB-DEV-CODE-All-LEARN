//Arthmetic operator
let a = 5;
let b = 2;

console.log("a + b =", a + b);   // Addition → 7
console.log("a - b =", a - b);   // Subtraction → 3
console.log("a * b =", a * b);   // Multiplication → 10
console.log("a / b =", a / b);   // Division → 2.5
console.log("a % b =", a % b);   // Modulus → 1
console.log("a ** b =", a ** b); // Exponent → 25
