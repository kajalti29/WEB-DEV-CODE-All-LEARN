// Increment operator
let a = 5;
let b = 2;

console.log("Before increment: a =", a, "& b =", b);
a = a + 1;
a++;
console.log("After increment: a =", a);


// Decrement operator
a = 5; // Reusing 'a' without declaring again
b = 2;

console.log("Before decrement: a =", a, "& b =", b);
a = a - 1;
a--;
console.log("After decrement: a =", a);







